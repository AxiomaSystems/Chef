function recipe(
  {
    name,
    cuisine,
    description,
    imageUrl,
    tags,
    ingredients,
    steps,
    servings = 4,
    calories = 520,
    protein = 24,
    carbs = 58,
    fat = 21,
  },
  index,
) {
  return {
    name,
    cuisine,
    description,
    coverImageUrl:
      imageUrl === null ? null : (imageUrl ?? fallbackImageUrl(index)),
    nutritionData: {
      calories,
      protein_g: protein,
      carbs_g: carbs,
      fat_g: fat,
    },
    servings,
    tags,
    ingredients: ingredients.map(
      ([canonicalIngredient, amount, unit, displayIngredient], sortOrder) => ({
        canonicalIngredient,
        amount,
        unit,
        displayIngredient: displayIngredient ?? canonicalIngredient,
        sortOrder,
      }),
    ),
    steps: steps.map((whatToDo, index) => ({
      stepNumber: index + 1,
      whatToDo,
    })),
  };
}

